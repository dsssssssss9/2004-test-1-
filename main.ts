makerbit.connectLcd(39)
makerbit.setLcdBacklight(LcdBacklight.On)
basic.forever(function () {
    makerbit.showStringOnLcd2004("Hello Frank", makerbit.position2004(LcdPosition2004.Pos1), 20, TextOption.AlignCenter)
    basic.pause(1000)
    makerbit.clearLcd2004()
    makerbit.showStringOnLcd2004("This is a 20 * 16", makerbit.position2004(LcdPosition2004.Pos21), 20, TextOption.AlignCenter)
    basic.pause(1000)
    makerbit.clearLcd2004()
    makerbit.showStringOnLcd2004("I2C LCD Display!", makerbit.position2004(LcdPosition2004.Pos41), 20, TextOption.AlignCenter)
    basic.pause(1000)
    makerbit.clearLcd2004()
    makerbit.showStringOnLcd2004("It Works with 3.3V!!!!", makerbit.position2004(LcdPosition2004.Pos61), 20, TextOption.AlignCenter)
    basic.pause(2000)
})
